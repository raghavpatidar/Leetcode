#include<bits/stdc++.h>
using namespace std;

class Solution {
public:
    int largestAltitude(vector<int>& gain) {
        int pref = 0, ans = 0;
        for (auto i : gain) {
            pref += i;
            ans = max(ans, pref);
        }
        return ans;
    }
};